-- 016_realtime_role_check.sql
--
-- Read role from public.profiles instead of the JWT's app_metadata so
-- promoting / demoting a user takes effect on the very next request,
-- not the next session refresh.
--
-- Background
-- ----------
-- Prior policies pulled the role from
--   auth.jwt()->'app_metadata'->>'role'
-- which is set by `sync_role_to_app_metadata` whenever a profile is
-- updated. The trigger correctly writes to auth.users.raw_app_meta_data,
-- but the user's currently-issued JWT still carries the OLD role until
-- they re-authenticate or the access token rotates. As a result, a
-- merchant who was just approved by an admin could see fresh data in
-- server actions (which read public.profiles directly via
-- getCurrentUser) but their writes would fail at the RLS layer with
-- the stale JWT, producing the user-facing "approved but still can't
-- create challenges" symptom.
--
-- Fix
-- ---
-- Route every role-based RLS check through public.current_user_role(),
-- a SECURITY DEFINER helper that queries public.profiles. The JWT
-- remains the auth boundary (auth.uid()), but the role decision is
-- always read from the source of truth so role changes propagate to
-- RLS on the very next query. The function is STABLE, so Postgres
-- caches it within a query plan -- the overhead is one extra index
-- lookup per RLS query, not per row.
--
-- The sync trigger on profiles -> auth.users.raw_app_meta_data is left
-- intact so any future JWT-side consumers (e.g. external services or
-- the supabase-js client doing local checks) still see the latest
-- value once the token rotates.

-- ????????????????????????????????????????????????????????????????????
-- Helper
-- ????????????????????????????????????????????????????????????????????

CREATE OR REPLACE FUNCTION public.current_user_role()
RETURNS TEXT
LANGUAGE SQL
STABLE
SECURITY DEFINER
SET search_path = ''
AS $$
  SELECT role::text FROM public.profiles WHERE id = auth.uid()
$$;

COMMENT ON FUNCTION public.current_user_role() IS
  'Returns the current authenticated user role from public.profiles. '
  'Used by RLS to avoid stale role values in the JWT after an admin '
  'role change.';

GRANT EXECUTE ON FUNCTION public.current_user_role() TO anon, authenticated;

-- ????????????????????????????????????????????????????????????????????
-- profiles
-- ????????????????????????????????????????????????????????????????????

DROP POLICY IF EXISTS "Admins can view all profiles" ON public.profiles;
CREATE POLICY "Admins can view all profiles"
  ON public.profiles FOR SELECT
  TO authenticated
  USING (public.current_user_role() IN ('admin', 'superadmin'));

DROP POLICY IF EXISTS "Admins can view all profiles for management" ON public.profiles;
CREATE POLICY "Admins can view all profiles for management"
  ON public.profiles FOR SELECT
  TO authenticated
  USING (public.current_user_role() IN ('admin', 'superadmin'));

DROP POLICY IF EXISTS "Admins can update profiles" ON public.profiles;
CREATE POLICY "Admins can update profiles"
  ON public.profiles FOR UPDATE
  TO authenticated
  USING (public.current_user_role() IN ('admin', 'superadmin'))
  WITH CHECK (public.current_user_role() IN ('admin', 'superadmin'));

DROP POLICY IF EXISTS "Admins can update profiles for role changes" ON public.profiles;
CREATE POLICY "Admins can update profiles for role changes"
  ON public.profiles FOR UPDATE
  TO authenticated
  USING (public.current_user_role() IN ('admin', 'superadmin'))
  WITH CHECK (public.current_user_role() IN ('admin', 'superadmin'));

